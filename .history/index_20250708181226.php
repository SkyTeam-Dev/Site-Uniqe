                                fetch(selectedAPI.url, {
                                        method: "GET",
                                        headers: {
                                            "Accept": "application/json",
                                            "Content-Type": "application/json"
                                        }
                                    })
                                    .then(response => {
                                        if (!response.ok) throw new Error("Erro HTTP");
                                        return response.json();
                                    })
                                    .then(data => processarResposta(data, selectedAPI))
                                    .catch(err => {
                                        logDebug("Erro na " + selectedAPI.name + ": " + err
                                            .message);
                                        tryNextAPI(index + 1);
                                    });
                            }, index * 2500);
